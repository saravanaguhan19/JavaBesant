package com.datatypedemo;

public class DatatypeDemo {
	public static void main(String[] args) {

		// byte datatype
		byte num1 = 25;
		System.out.println(num1);

		// short datatype
		short num2 = 3500;
		System.out.println(num2);

		// int dataype
		int num3 = 39403303;
		System.out.println(num3);

		// long datatype
		long num4 = 3949403030339L;
		System.out.println(num4);

		// float datatype
		float num5 = 256.7856783456F;
		System.out.println(num5);

		// double datatype

		double num6 = 356789.2930303048475;
		System.out.println(num6);

		// char datatype

		char alpha = 'v';
		System.out.println(alpha);

		char val = '9';
		System.out.println(val);

		char spl = '$';
		System.out.println(spl);

		// string datatype

		String name = "saravana guhan";
		System.out.println(name);

		// boolean datatype

		boolean isNum = true;
		System.out.println(isNum);

	}


}
